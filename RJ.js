function filter(filterType) {
    const elements = document.querySelectorAll('.element');
    
    elements.forEach(element => {
        element.style.display = 'none'; // Oculta todos os elementos
    });

    const filteredElements = document.querySelectorAll(`.element.${filterType}`);
    filteredElements.forEach(element => {
        element.style.display = ''; // Exibe apenas os elementos do tipo selecionado
    });
}

